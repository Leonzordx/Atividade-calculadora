def somar(a, b):
	return (a + b)

def subtrair(a, b):
	return (a - b)

def multiplicar(a, b):
	return (a * b)

def dividir(a, b):
	return (a / b)

num1 = float(input("Insira o primeiro numero: "))
num2 = float(input("Insira o segundo numero: "))

somar(num1, num2)
subtrair(num1, num2)
multiplicar(num1, num2)
dividir(num1, num2)